//---------------------------------------------------------------------------
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include "Copyright.h"
#include "ConcordMain.h"
#include "IOBuffer.h"
//---------------------------------------------------------------------------


void usage() {
printf("%s",COPYRIGHT);
printf("Usage: Concord <concor> <font> <fontsize> <l> <r> <order> <mode> <alph> [-thai]\n");
printf("   <concord> : the concord.ind file generated by the locate program\n");
printf("   <font> : the font you want to use\n");
printf("   <fontsize> : the font size\n");
printf("   <l> : left context length in chars\n");
printf("   <r> : right context length in chars\n");
printf("   <order> : sort order:\n");
printf("           TO : text order\n");
printf("           LC : left, center\n");
printf("           LR : left, right\n");
printf("           CL : center, left\n");
printf("           CR : center, right\n");
printf("           RL : right, left\n");
printf("           RC : right, center\n");
printf("           NULL : specifies no order (used for non-html <mode>)\n");
printf("   <mode> : the mode to be used:\n");
printf("            html : produces an HTML concordance file\n");
printf("            text : produces a plain text concordance file\n");
printf("            glossanet=<script> : produces a glossanet HTML concordance file\n");
printf("            (txt) : produces a file named (txt) which is the SNT file\n");
printf("                    merged with the match results\n");
printf("   <alph> : the char order file used for sorting\n");
printf("            NULL if no alphabet is given\n");
printf("   [-thai] : option to use for thai concordances\n");
printf("\nExtracts the matches stored in <concor>, and stores them into a UTF-8\n");
printf("HTML file saved in the <concor> directory or produces a text file, according\n");
printf("to the <mode> parameter\n");
printf("\nExamples:\n");
printf("Concord tutu.ind \"Courier New\" 12 40 40 TO html \"d:\\My dir\\alph.txt\"\n");
printf("    -> produces an HTML file\n");
printf("\n");
printf("Concord tutu.ind NULL 0 0 0 NULL \"C:\\My dir\\RES.SNT\" NULL\n");
printf("    -> produces a text file named \"C:\\My dir\\RES.SNT\"\n");
}


int main(int argc, char **argv) {
/* Every Unitex program must start by this instruction,
 * in order to avoid display problems when called from
 * the graphical interface */
setBufferMode();
if (argc!=9 && argc!=10 && argc!=11) {
	usage();
	return 0;
}
/* We call an artificial main function located in 'ConcordMain'. This
 * trick allows to use the functionalities of the 'Concord' program
 * without having to launch an external process.
 */
return main_concord_cpp(argc,argv);
}
